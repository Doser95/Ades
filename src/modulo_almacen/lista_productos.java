    public void mostrar_tabla() {

        DefaultTableModel nm = new DefaultTableModel();
        nm.addColumn("Nombre del producto");
        nm.addColumn("Tipo");
        nm.addColumn("Clave");
        nm.addColumn("Stock minimo");
        nm.addColumn("Existencia");
        nm.addColumn("Stock maximo");
        nm.addColumn("Localizaci√≥n");
        
        tabla1.setModel(nm);

        String var[] = new String[7];
        try {
            Statement ps = con.createStatement();
            ResultSet rs = ps.executeQuery("select nombre_prod, TIPO, clave, stock_minimo, existencia, stock_maximo, localizacion from productos");
            while (rs.next()) {
                var[0] = rs.getString(1);
                var[1] = rs.getString(2);
                var[2] = rs.getString(3);
                var[3] = rs.getString(4);
                var[4] = rs.getString(5);
                var[5] = rs.getString(6);
                var[6] = rs.getString(7);
                nm.addRow(var);

            }
            tabla1.setModel(nm);

        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(lista_productos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

